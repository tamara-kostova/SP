/*Да се напише програма која пресметува просечна оцена во семестар. Програмата чита 5 цели броја. 
Треба да се испечати еден реален број на две децимали, просек на прочитаните броеви.*/
#include <stdio.h>
int main(){
    int a[4];
    float prosek=0;
    for (int i=0; i<5; i++){
        scanf("%d", &a[i]);
        prosek+=a[i];
    }
    printf("%.2f", prosek/5);
return 0;
    }
