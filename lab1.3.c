/*Да се напише програма која вчитува од стандарден влез два децимални броја (маса во кг и висина во цм) и пресметува и печати на стандарден излез индекс на телесна маса 
по формулата:
$BMI = \frac{masa}{visina * visina}$
Висината претходно треба да се претвори од сантиметри во метри.*/
#include <stdio.h>
int main(){
    float m, v, vm;
    scanf ("%f %f", &m, &v);
    vm=v/100;
    printf ("%.2f", m/(vm*vm));
return 0;
    }
